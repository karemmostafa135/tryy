#ifndef EXT_INTERRUPT_H_
#define EXT_INTERRUPT_H_

#define  DISABLE_INTERRUPT	0
#define  ENABLE_INTERRUPT	1

/* INT0 sense control masks */
#define  INT0_RESET_SENSE_MASK		 0b11111100
#define  INT0_LOW_LEVEL_MASK		 0b00000000
#define  INT0_LOGICAL_CHANGE_MASK    0b00000001
#define  INT0_FAILING_EDGE_MASK		 0b00000010
#define  INT0_RISING_EDGE_MASK		 0b00000011

/* INT1 sense control masks */
#define  INT1_RESET_SENSE_MASK		 0b11111001
#define  INT1_LOW_LEVEL_MASK		 0b00000000
#define  INT1_LOGICAL_CHANGE_MASK    0b00000100
#define  INT1_FAILING_EDGE_MASK		 0b00001000
#define  INT1_RISING_EDGE_MASK		 0b00001100

/* INT2 sense control masks */
#define  INT2_RESET_SENSE_MASK		 0b10111111
#define  INT2_FAILING_EDGE_MASK		 0b00000000
#define  INT2_RISING_EDGE_MASK		 0b01000000

void EXT_voidInterrupt_Init(void);

#endif /* EXT_INTERRUPT_H_ */